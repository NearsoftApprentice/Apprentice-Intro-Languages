
function sumarDosNumeros(valor1, valor2){
	
	return valor1 + valor2;	
}


function restarDosNumeros(valor1, valor2){
	
	return valor1 - valor2;	
}


function multiplicarDosNumeros(valor1, valor2){
	
	return valor1 * valor2;	
}

function dividirDosNumeros(valor1, valor2){
	
	return valor1 / valor2;	
}
